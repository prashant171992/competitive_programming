    public int matrixScore(int[][] A) {

        // Row checking
        int [][] temp = A;
        for (int i = 0; i < A.length; ++i) {
            if (A[i][0] == 0)
                temp = toggleHelper(A, i, -1);
        }

        A = temp;

        // Column checking

        for (int j = A[0].length-1; j >= 0; --j) {
            int count = 0;
            for (int k = 0; k < A.length; ++k) {
                if (A[k][j] == 0) {
                    count++;
                } else {
                    count--;
                }
            }
            if (count > 0) {
                temp = toggleHelper(A, -1, j);
            }
        }

        A = temp;

        int result = 0;
        for (int i = 0; i < A.length; i++) {
            int num = 0;
            int counter = 0;
            for (int j = A[0].length-1; j >= 0; j--){
                num += A[i][j] * Math.pow(2, counter);
                counter++;
            }
            result+=num;
        }

        return result;
    }

    int[][] toggleHelper(int[][] A, int row, int column) {

        if (row == -1) {
            for (int i = 0; i < A.length; ++i) {
                A[i][column] = Math.abs(A[i][column] - 1);
            }
        }
        if (column == -1) {
            for (int i = 0; i < A[0].length; ++i) {
                A[row][i] = Math.abs(A[row][i] - 1);
            }
        }

        return A;

    }

