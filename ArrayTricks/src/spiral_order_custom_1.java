    private List<List<Integer>> generateSpiral2DArray(int input) {
        List<List<Integer>> result = new ArrayList<>();
        int [][]dir = {{0,1}, {1,0}, {0,-1}, {-1,0}};
        int shift = 0;
        int x = 0;
        int y = 0;
        for (int i = 0; i < input; i++){
            ArrayList<Integer> arrayList = new ArrayList<>();
            for (int j = 0; j < input; ++j) {
                arrayList.add(0);
            }
            result.add(arrayList);
        }
        for (int i = 1; i <= input*input; ++i) {
            result.get(x).set(y, i);
            int nextX = x + dir[shift][0];
            int nextY = y + dir[shift][1];
            if (nextX >= input || nextY >= input || nextX < 0 || nextY < 0 || (nextX < result.get(x).size() && nextY < result.get(x).size() && result.get(nextX).get(nextY) != 0)) {
                shift = (shift + 1) %4;
                nextX = x + dir[shift][0];
                nextY = y + dir[shift][1];
            }
            x = nextX;
            y = nextY;
        }
        return result;
    }

    private BigInteger modulo(BigInteger a, BigInteger b, BigInteger c) {
        BigInteger t1 = a.mod(c);
        BigInteger t2 = b.mod(c);
        BigInteger t3 = t1.multiply(t2);
        return t3.mod(c);
    }


