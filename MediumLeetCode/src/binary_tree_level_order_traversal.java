    private BigInteger modulo(BigInteger a, BigInteger b, BigInteger c) {
        BigInteger t1 = a.mod(c);
        BigInteger t2 = b.mod(c);
        BigInteger t3 = t1.multiply(t2);
        return t3.mod(c);
    }

    public List<List<Integer>> levelOrder(TreeNode root) {
        if (root == null) return new ArrayList<>();
        Queue<TreeNode> queue = new LinkedList<>();
        queue.add(root);

        List<List<Integer>> result = new ArrayList<>();

        while (!queue.isEmpty()) {
            ArrayList<TreeNode> childNodes = new ArrayList<>();
            List<Integer> treeNodes = new ArrayList<>();
            while (!queue.isEmpty()) {
                TreeNode treeNode = queue.remove();
                treeNodes.add(treeNode.val);
                if (treeNode.left != null) {
                    childNodes.add(treeNode.left);
                }
                if (treeNode.right != null) {
                    childNodes.add(treeNode.right);
                }
            }
            result.add(treeNodes);
            queue.addAll(childNodes);
        }
        return result;
    }


